                    <h4>WebDesigner</h4>
                    <p>
                        Le webdesigner conçoit et réalise l'identité visuelle d'un site internet pour lequel il crée tous les éléments graphiques (illustrations, bannières, animations…)    selon les désirs du client et en fonction du public visé, des impératifs du marketing et des contraintes techniques. Objectif : séduire les futurs utilisateurs du  site.
                        Sous la responsabilité du chef de projet ou du directeur artistique, le web designer définit le cahier des charges du produit : ergonomie, mode de navigation, arborescence, scénarisation, identité visuelle et sonore, charte graphique… Il s’occupe aussi du design des interfaces dédiées au e-commerce et au e-marketing.
                        Le webdesigner dessine (sur papier et à la palette graphique), crée des maquettes et retouche des pages internet, mais il n’est pas un simple exécutant : il a une vision d’ensemble du produit final.
                        Le web designer peut travailler dans une agence web spécialisée en communication d’entreprise ou en publicité, dans un studio de création multimédia, dans une maison d’édition en ligne, une SSII (société de services en ingénierie informatique). Certains font le choix de s'installer à leur compte. 
                        Novateur, curieux, méthodique et organisé, le web designer sait travailler en équipe et souvent sous pression.
                    </p>
                    <small>(Niveau: V)</small>
