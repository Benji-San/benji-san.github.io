                    <h4>Technicien d’Assistance en Informatique</h4>
                    <p>Le(a) technicien(ne) d’assistance en informatique participe
                        à l’implantation, à la diffusion et au développement de
                        l’informatique auprès des utilisateurs.
                        À ce titre, il installe, configure le matériel, les systèmes
                        d’exploitation, les logiciels et périphériques dans un environnement
                        informatique.
                        Il met en service et assure la maintenance des équipements
                        informatiques reliés en réseau.
                        Il assiste les clients à distance ou sur le site même (en intervention
                        ou en régie), afin de résoudre les dysfonctionnements. Il conseille
                        et peut former les utilisateurs dans la perspective d’une utilisation
                        optimale de leurs outils bureautiques et informatiques.
                        Seul ou en équipe, le technicien intervient sur des équipements :
                        PC de bureau, portable, téléphonie IP, Box….
                    </p>
